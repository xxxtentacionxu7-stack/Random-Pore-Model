%% plot_900_RPM_CO2_H2O_fit
% RPM fitting figure for CO2 and H2O at 900 C:
% experiment vs RPM fit in one publication-style panel.

clear; clc; close all;

script_dir = fileparts(mfilename('fullpath'));
if ~isempty(script_dir)
    cd(script_dir);
end

params.T_exp = 900;
params.T_iso_start = 885;
params.smooth_span = 11;
params.psi_init = 3.0;

co2 = fitOneAtmosphere('CO2_900C.txt', 'CO_2', params);
h2o = fitOneAtmosphere('H2O_900C.txt', 'H_2O', params);

style = rpmPaperStyle();

fig = figure('Name', '900 C RPM fit: CO2 and H2O', ...
    'Color', 'w', 'Position', [80 80 660 430]);
ax = axes(fig);
hold(ax, 'on');

co2_color = [0 114 178] ./ 255;
h2o_color = [213 94 0] ./ 255;

plot(ax, co2.rpm.t, co2.rpm.X_data, 'o', ...
    'Color', co2_color, ...
    'MarkerFaceColor', 'none', ...
    'MarkerSize', 2.2, ...
    'LineWidth', 0.45, ...
    'DisplayName', 'CO_2 experiment');
plot(ax, co2.rpm.t, co2.rpm.X_fit, '-', ...
    'Color', co2_color, ...
    'LineWidth', style.lineWidth, ...
    'DisplayName', sprintf('CO_2 RPM fit (R^2 = %.4f)', co2.rpm.R2));

plot(ax, h2o.rpm.t, h2o.rpm.X_data, 's', ...
    'Color', h2o_color, ...
    'MarkerFaceColor', 'none', ...
    'MarkerSize', 2.2, ...
    'LineWidth', 0.45, ...
    'DisplayName', 'H_2O experiment');
plot(ax, h2o.rpm.t, h2o.rpm.X_fit, '--', ...
    'Color', h2o_color, ...
    'LineWidth', style.lineWidth, ...
    'DisplayName', sprintf('H_2O RPM fit (R^2 = %.4f)', h2o.rpm.R2));

xlabel(ax, 'Isothermal reaction time (min)', ...
    'FontName', style.fontName, 'FontSize', style.labelFontSize);
ylabel(ax, 'Conversion, X', ...
    'FontName', style.fontName, 'FontSize', style.labelFontSize);
ylim(ax, [0 1.04]);
xlim(ax, [0 max([co2.rpm.t(:); h2o.rpm.t(:)])]);

legend(ax, 'Location', 'southeast', 'Box', 'off', ...
    'FontName', style.fontName, 'FontSize', style.legendFontSize, ...
    'Interpreter', 'tex');

text(ax, 0.03, 0.94, '900 C', 'Units', 'normalized', ...
    'FontName', style.fontName, 'FontSize', style.panelFontSize, ...
    'FontWeight', 'bold');

param_text = sprintf(['CO_2: k = %.4f min^{-1}, \\psi = %.3f\n' ...
                      'H_2O: k = %.4f min^{-1}, \\psi = %.3f'], ...
    co2.rpm.k, co2.rpm.psi, h2o.rpm.k, h2o.rpm.psi);
text(ax, 0.09, 0.53, param_text, 'Units', 'normalized', ...
    'FontName', style.fontName, 'FontSize', style.annotationFontSize, ...
    'VerticalAlignment', 'top', 'Interpreter', 'tex');

applyRpmPaperAxes(fig, style);
ExportFigure(fig, 'Paper_Fig_900C_RPM_CO2_H2O_experiment_vs_fit');

plotSingleRpmFigure(co2, 'CO_2', co2_color, '-', 'o', style, ...
    'Paper_Fig_900C_RPM_CO2_experiment_vs_fit');
plotSingleRpmFigure(h2o, 'H_2O', h2o_color, '--', 's', style, ...
    'Paper_Fig_900C_RPM_H2O_experiment_vs_fit');

fprintf('\nRPM fitting figure generated:\n');
fprintf('  Paper_Fig_900C_RPM_CO2_H2O_experiment_vs_fit.png/pdf/eps\n');
fprintf('  Paper_Fig_900C_RPM_CO2_experiment_vs_fit.png/pdf/eps\n');
fprintf('  Paper_Fig_900C_RPM_H2O_experiment_vs_fit.png/pdf/eps\n');
fprintf('\nFit summary:\n');
fprintf('  CO2: k = %.6f min^-1, psi = %.6f, R2 = %.6f\n', co2.rpm.k, co2.rpm.psi, co2.rpm.R2);
fprintf('  H2O: k = %.6f min^-1, psi = %.6f, R2 = %.6f\n', h2o.rpm.k, h2o.rpm.psi, h2o.rpm.R2);

function out = fitOneAtmosphere(filename, label, params)
    fprintf('\n========== RPM fit: %s ==========\n', label);
    data = ReadTGA(filename);
    mass_ash = mean(data.m(end-9:end));
    data = CalculateConversion(data, mass_ash, params.smooth_span);
    iso = ExtractIsothermalSegment(data, params.T_iso_start);
    rpm = FitRPM(iso, params.psi_init);

    out.label = label;
    out.filename = filename;
    out.mass_ash = mass_ash;
    out.data = data;
    out.iso = iso;
    out.rpm = rpm;
end

function plotSingleRpmFigure(fit_result, label, color, fit_line_style, marker, style, basename)
    rpm = fit_result.rpm;
    fig = figure('Name', ['900 C RPM fit: ' label], ...
        'Color', 'w', 'Position', [80 80 560 390]);
    ax = axes(fig);
    hold(ax, 'on');

    plot(ax, rpm.t, rpm.X_data, marker, ...
        'Color', color, ...
        'MarkerFaceColor', 'none', ...
        'MarkerSize', 2.2, ...
        'LineWidth', 0.45, ...
        'DisplayName', [label ' experiment']);
    plot(ax, rpm.t, rpm.X_fit, fit_line_style, ...
        'Color', color, ...
        'LineWidth', style.lineWidth, ...
        'DisplayName', [label ' RPM fit']);

    xlabel(ax, 'Isothermal reaction time (min)', ...
        'FontName', style.fontName, 'FontSize', style.labelFontSize);
    ylabel(ax, 'Conversion, X', ...
        'FontName', style.fontName, 'FontSize', style.labelFontSize);
    ylim(ax, [0 1.04]);
    xlim(ax, [0 max(rpm.t(:))]);

    legend(ax, 'Location', 'southeast', 'Box', 'off', ...
        'FontName', style.fontName, 'FontSize', style.legendFontSize, ...
        'Interpreter', 'tex');

    text(ax, 0.05, 0.93, ['900 C, ' label], 'Units', 'normalized', ...
        'FontName', style.fontName, 'FontSize', style.panelFontSize, ...
        'FontWeight', 'bold', 'Interpreter', 'tex');

    param_text = sprintf('k = %.4f min^{-1}\n\\psi = %.3f\nR^2 = %.4f', ...
        rpm.k, rpm.psi, rpm.R2);
    text(ax, 0.58, 0.48, param_text, 'Units', 'normalized', ...
        'FontName', style.fontName, 'FontSize', style.annotationFontSize, ...
        'VerticalAlignment', 'top', 'Interpreter', 'tex');

    applyRpmPaperAxes(fig, style);
    ExportFigure(fig, basename);
end

function style = rpmPaperStyle()
    style.fontName = 'Arial';
    style.labelFontSize = 10;
    style.axesFontSize = 9;
    style.legendFontSize = 8;
    style.annotationFontSize = 8.5;
    style.panelFontSize = 11;
    style.lineWidth = 1.8;
end

function applyRpmPaperAxes(fig, style)
    ax = findall(fig, 'Type', 'axes');
    for i = 1:numel(ax)
        set(ax(i), ...
            'FontName', style.fontName, ...
            'FontSize', style.axesFontSize, ...
            'LineWidth', 1.0, ...
            'Box', 'off', ...
            'TickDir', 'out', ...
            'TickLength', [0.012 0.012], ...
            'Layer', 'top', ...
            'XColor', [0.1 0.1 0.1], ...
            'YColor', [0.1 0.1 0.1]);
        grid(ax(i), 'off');
    end
end

